----------------------------------------------------------------------------------------------------
-- dinv migration module
--
-- Migrates data from old aard_inventory (plugin ID 88c86ea252fc1918556df9fe) serialized
-- Lua state files into dinv's SQLite database.
--
-- The old state files are never modified. Migration replaces the current dinv database
-- (a backup is created first). The migration is atomic: if any step fails, the database
-- is rolled back and the backup is restored.
--
-- Old state files are loaded using Lua's loadstring(). This is safe because the files
-- were generated by the user's own plugin installation and contain only Lua table
-- assignments. A line-by-line parser would be fragile and would essentially re-implement
-- a subset of the Lua parser; loadstring is the most reliable approach.
--
-- Functions:
--   inv.migrate.getOldDir()       -- Get path to old aard_inventory state directory
--   inv.migrate.detect()          -- Detect old state files, return summary
--   inv.migrate.execute()         -- Run the migration
--   inv.migrate.loadStateFile()   -- Load a single .state file via loadstring
----------------------------------------------------------------------------------------------------

inv.migrate = {}

-- Old aard_inventory plugin ID
local oldPluginId = "88c86ea252fc1918556df9fe"

-- State files we can migrate, in order of processing.  Order matters: things that
-- reference an objId must come after items so any later cross-checks succeed.
-- Equip bonuses are deliberately not migrated -- they are a deterministic cache
-- of (levelBonus - spellBonus) that inv.statBonus.get recomputes on demand;
-- v3.0097 stopped persisting them, so importing old equip rows would be wasted
-- work (the next save's DELETE FROM stat_bonuses wipes them anyway).
local stateFiles = {
  { file = "inv-items.state",          desc = "Items",         root = "inv.items.table" },
  { file = "inv-priorities.state",     desc = "Priorities",    root = "inv.priority.table" },
  { file = "inv-set.state",            desc = "Sets",          root = "inv.set.table" },
  { file = "inv-snapshot.state",       desc = "Snapshots",     root = "inv.snapshot.table" },
  { file = "inv-consume.state",        desc = "Consumables",   root = "inv.consume.table" },
  { file = "inv-cache-custom.state",   desc = "Custom cache",  root = "inv.cache.custom.table" },
  { file = "inv-cache-frequent.state", desc = "Frequent cache",root = "inv.cache.frequent.table" },
  { file = "inv-cache-recent.state",   desc = "Recent cache",  root = "inv.cache.recent.table" },
  { file = "inv-stats-spells.state",   desc = "Spell bonuses", root = "inv.statBonus.spellBonus" },
  { file = "inv-config.state",         desc = "Config",        root = "inv.config.table" },
}


----------------------------------------------------------------------------------------------------
-- Error logging
--
-- loadStateFile failures are not fatal (we commit whatever else migrated successfully) so the
-- original error message from loadstring/pcall is easy to miss in the chat window. Mirror it
-- to a file next to dinv.db so the user can inspect exactly why a given state file did not load.
----------------------------------------------------------------------------------------------------

local function getErrorLogPath()
  return dinv_db.getDir() .. "migrate-errors.log"
end

local function writeLoadError(filePath, errMsg)
  local logPath = getErrorLogPath()
  local f = io.open(logPath, "a")
  if not f then
    return  -- best-effort; don't block migration if the log can't be written
  end
  f:write(string.format("[%s] %s\n  %s\n\n",
                        os.date("%Y-%m-%d %H:%M:%S"),
                        filePath or "?",
                        errMsg or "unknown error"))
  f:close()
end


