-- Path helpers
----------------------------------------------------------------------------------------------------

-- Returns the base state directory (without plugin-specific suffix).
-- Derived from pluginStatePath by stripping "dinv-{currentPluginId}".
function inv.migrate.getStateBase()
  local suffix = "dinv-" .. pluginId
  local pos = pluginStatePath:find(suffix, 1, true)
  if not pos then return nil end
  return pluginStatePath:sub(1, pos - 1)
end


-- Returns the path to the old aard_inventory state directory for the current character.
function inv.migrate.getOldDir()
  local base = inv.migrate.getStateBase()
  if not base then return nil end

  local charName = dbot.gmcp.getName()
  if not charName then return nil end

  return base .. "dinv-" .. oldPluginId .. "\\" .. charName .. "\\current\\"
end


----------------------------------------------------------------------------------------------------
-- State file loader
----------------------------------------------------------------------------------------------------

-- Load a single .state file using loadstring.
-- Returns the root table on success, or nil and an error message on failure.
--
-- The file format is:
--   Line 1: version number (always "1")
--   Line 2: SHA1 hash (integrity check, not validated during migration)
--   Line 3+: Lua table assignment code
--
-- We skip lines 1-2 and execute the rest. The code assigns into global variables
-- (e.g., inv.items.table = {}; inv.items.table[id].field = value).
-- We set up a sandboxed environment with the necessary root tables pre-declared,
-- then extract the populated table after execution.
function inv.migrate.loadStateFile(filePath, rootPath)
  local f = io.open(filePath, "r")
  if not f then
    return nil, "Cannot open file: " .. filePath
  end

  local content = f:read("*a")
  f:close()

  if not content or content == "" then
    return nil, "File is empty: " .. filePath
  end

  -- Skip lines 1 (version) and 2 (hash)
  local lineNum = 0
  local codeStart = nil
  for i = 1, #content do
    if content:sub(i, i) == "\n" then
      lineNum = lineNum + 1
      if lineNum == 2 then
        codeStart = i + 1
        break
      end
    end
  end

  if not codeStart then
    return nil, "File has fewer than 3 lines: " .. filePath
  end

  local code = content:sub(codeStart)

  -- Build a sandbox environment with the root path pre-declared.
  -- For "inv.items.table", we need: env.inv = { items = { table = {} } }
  -- The loadstring code will then populate env.inv.items.table.
  local env = {}

  -- Parse the root path (e.g., "inv.items.table") into nested tables
  local parts = {}
  for part in rootPath:gmatch("[^%.]+") do
    table.insert(parts, part)
  end

  -- Create nested structure
  local current = env
  for i = 1, #parts do
    current[parts[i]] = {}
    current = current[parts[i]]
  end

  -- Allow access to standard globals needed by the serialized code
  setmetatable(env, { __index = _G })

  -- Load and execute the code in the sandbox
  local fn, err = loadstring(code)
  if not fn then
    return nil, "Failed to parse state file: " .. (err or "unknown error")
  end

  setfenv(fn, env)
  local ok, execErr = pcall(fn)
  if not ok then
    return nil, "Failed to execute state file: " .. (execErr or "unknown error")
  end

  -- Extract the root table from the environment
  current = env
  for i = 1, #parts do
    current = current[parts[i]]
    if not current then
      return nil, "Root table " .. rootPath .. " was not populated"
    end
  end

  return current
end


----------------------------------------------------------------------------------------------------
-- Detection
----------------------------------------------------------------------------------------------------

-- Detect old state files and return a summary table.
-- Returns { found = true/false, dir = path, files = { {file, desc, exists, path}, ... } }
function inv.migrate.detect()
  local result = { found = false, dir = nil, files = {} }

  local oldDir = inv.migrate.getOldDir()
  if not oldDir then
    return result
  end

  result.dir = oldDir

  if not dbot.fileExists(oldDir) then
    return result
  end

  for _, sf in ipairs(stateFiles) do
    local path = oldDir .. sf.file
    local exists = dbot.fileExists(path)
    table.insert(result.files, {
      file   = sf.file,
      desc   = sf.desc,
      exists = exists,
      path   = path,
      root   = sf.root,
    })
    if exists then
      result.found = true
    end
  end

  return result
end


----------------------------------------------------------------------------------------------------
-- Migration: Items
----------------------------------------------------------------------------------------------------

local function migrateItems(data)
  local db = dinv_db.handle
  local count = 0

  for objId, entry in pairs(data) do
    local query = dinv_db.buildItemInsert("items", objId, entry)
    db:exec(query)
    if dinv_db.dbcheck(db:errcode(), db:errmsg(), query) then
      return nil, "Failed to insert item " .. tostring(objId)
    end
    count = count + 1
  end

  return count
end


----------------------------------------------------------------------------------------------------
-- Migration: Priorities
----------------------------------------------------------------------------------------------------

local function migratePriorities(data)
  local db = dinv_db.handle
  local count = 0

  for priorityName, blocks in pairs(data) do
    local query = string.format("INSERT INTO priorities (name) VALUES (%s)",
                                dinv_db.fixsql(priorityName))
    db:exec(query)
    if dinv_db.dbcheck(db:errcode(), db:errmsg(), query) then
      return nil, "Failed to insert priority " .. priorityName
    end

    local priorityId = db:last_insert_rowid()

    for blockIdx, block in ipairs(blocks) do
      local columns = "priority_id, block_index, min_level, max_level"
      local values = string.format("%d, %d, %d, %d",
                                    priorityId, blockIdx,
                                    block.minLevel or 1, block.maxLevel or 291)

      if block.priorities then
        for luaKey, weight in pairs(block.priorities) do
          local sqlCol = inv.priority.luaToSql[luaKey]
          if sqlCol and weight ~= 0 then
            columns = columns .. ", " .. sqlCol
            values = values .. ", " .. tostring(weight)
          end
        end
      end

      query = string.format("INSERT INTO priority_blocks (%s) VALUES (%s)", columns, values)
      db:exec(query)
      if dinv_db.dbcheck(db:errcode(), db:errmsg(), query) then
        return nil, "Failed to insert priority block for " .. priorityName
      end
    end

    count = count + 1
  end

  return count
end


----------------------------------------------------------------------------------------------------
-- Migration: Sets
----------------------------------------------------------------------------------------------------

local function migrateSets(data)
  local db = dinv_db.handle
  local count = 0

  for priorityName, levels in pairs(data) do
    for level, equipSet in pairs(levels) do
      for wearLoc, itemData in pairs(equipSet) do
        local query = string.format(
          "INSERT INTO sets (priority_name, level, wear_loc, obj_id, score) VALUES (%s, %d, %s, %s, %s)",
          dinv_db.fixsql(priorityName),
          level,
          dinv_db.fixsql(wearLoc),
          dinv_db.fixnum(itemData.id),
          dinv_db.fixnum(itemData.score))
        db:exec(query)
        if dinv_db.dbcheck(db:errcode(), db:errmsg(), query) then
          return nil, "Failed to insert set " .. priorityName .. "[" .. level .. "]"
        end
        count = count + 1
      end
    end
  end

  return count
end


