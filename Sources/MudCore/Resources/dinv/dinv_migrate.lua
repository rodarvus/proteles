----------------------------------------------------------------------------------------------------
-- Migration: Stat bonuses (spells)
----------------------------------------------------------------------------------------------------

local function migrateSpellBonuses(data)
  local db = dinv_db.handle
  local count = 0
  local stats = { "int", "wis", "luck", "str", "dex", "con" }

  for level, levelData in pairs(data) do
    for _, stat in ipairs(stats) do
      local aveVal = (levelData.ave and levelData.ave[stat]) or nil
      local maxVal = (levelData.max and levelData.max[stat]) or nil
      if aveVal or maxVal then
        local query = string.format(
          "INSERT INTO stat_bonuses (bonus_type, level, stat_name, ave_val, max_val) VALUES ('spell', %d, %s, %s, %s)",
          level, dinv_db.fixsql(stat), dinv_db.fixnum(aveVal), dinv_db.fixnum(maxVal))
        db:exec(query)
        if dinv_db.dbcheck(db:errcode(), db:errmsg(), query) then
          return nil, "Failed to insert spell bonus"
        end
        count = count + 1
      end
    end
  end

  return count
end


----------------------------------------------------------------------------------------------------
-- Migration: Config
----------------------------------------------------------------------------------------------------

-- Config keys to migrate from old format. Maps old field name to new config key.
-- Only behavioral settings are migrated; format versions are not (they are meaningless
-- in the new system).
local configKeyMap = {
  isPromptEnabled = "config.isPromptEnabled",
  isBuildExecuted = "config.isBuildExecuted",
  doIgnoreKeyring = "config.doIgnoreKeyring",
  isRegenEnabled  = "config.isRegenEnabled",
  regenOrigObjId  = "config.regenOrigObjId",
  regenNewObjId   = "config.regenNewObjId",
  refreshPeriod   = "config.refreshPeriod",
  refreshEagerSec = "config.refreshEagerSec",
}

local function migrateConfig(data)
  local db = dinv_db.handle
  local count = 0

  for oldKey, newKey in pairs(configKeyMap) do
    local val = data[oldKey]
    if val ~= nil then
      local query = string.format(
        "INSERT OR REPLACE INTO config (key, value) VALUES (%s, %s)",
        dinv_db.fixsql(newKey), dinv_db.fixsql(tostring(val)))
      db:exec(query)
      if dinv_db.dbcheck(db:errcode(), db:errmsg(), query) then
        return nil, "Failed to insert config key " .. newKey
      end
      count = count + 1
    end
  end

  return count
end


----------------------------------------------------------------------------------------------------
-- Validation
----------------------------------------------------------------------------------------------------

local function validateCount(tableName, expected)
  local db = dinv_db.handle
  local actual = 0
  for row in db:nrows("SELECT COUNT(*) as cnt FROM " .. tableName) do
    actual = row.cnt
  end
  return actual, (actual == expected)
end


----------------------------------------------------------------------------------------------------
-- Main migration execution
----------------------------------------------------------------------------------------------------

function inv.migrate.execute()
  local detection = inv.migrate.detect()

  if not detection.found then
    dbot.info("No old aard_inventory state files found. Nothing to migrate.")
    return DRL_RET_MISSING_ENTRY
  end

  -- Create a backup of the current database before we nuke it
  dbot.info("Creating backup of current database before migration...")
  local backupRetval = dbot.backup.create("pre-migrate", nil)
  if backupRetval ~= DRL_RET_SUCCESS and backupRetval ~= DRL_RET_UNINITIALIZED then
    dbot.info("Warning: Could not create backup (return code: " ..
              dbot.retval.getString(backupRetval) .. ")")
    dbot.info("Proceeding with migration anyway...")
  end

  -- Close the database, delete it, and reopen (fresh schema)
  local dbPath = dinv_db.getPath()
  dinv_db.close()

  os.remove(dbPath)

  if not dinv_db.open() then
    dbot.error("Failed to reopen database after deletion. Migration aborted.")
    return DRL_RET_INTERNAL_ERROR
  end

  -- Run migration inside a single transaction
  local results = {}
  local migrationFailed = false
  local anyLoadFailed = false
  local failureMsg = nil

  local retval = dinv_db.transaction(function()

    -- Migrate each state file that exists
    for _, fileInfo in ipairs(detection.files) do
      if fileInfo.exists then
        dbot.info("Loading " .. fileInfo.desc .. " from " .. fileInfo.file .. "...")
        local data, loadErr = inv.migrate.loadStateFile(fileInfo.path, fileInfo.root)

        if not data then
          dbot.warn("Failed to load " .. fileInfo.desc .. ": " .. (loadErr or "unknown"))
          table.insert(results, { desc = fileInfo.desc, status = "FAILED", detail = loadErr })
          writeLoadError(fileInfo.path, loadErr)
          anyLoadFailed = true

        else
          local count, migrateErr

          if fileInfo.file == "inv-items.state" then
            count, migrateErr = migrateItems(data)
          elseif fileInfo.file == "inv-priorities.state" then
            count, migrateErr = migratePriorities(data)
          elseif fileInfo.file == "inv-set.state" then
            count, migrateErr = migrateSets(data)
          elseif fileInfo.file == "inv-snapshot.state" then
            count, migrateErr = migrateSnapshots(data)
          elseif fileInfo.file == "inv-consume.state" then
            count, migrateErr = migrateConsumables(data)
          elseif fileInfo.file == "inv-cache-custom.state" then
            count, migrateErr = migrateCacheCustom(data)
          elseif fileInfo.file == "inv-cache-frequent.state" then
            count, migrateErr = migrateCacheFrequent(data)
          elseif fileInfo.file == "inv-cache-recent.state" then
            count, migrateErr = migrateCacheRecent(data)
          elseif fileInfo.file == "inv-stats-spells.state" then
            count, migrateErr = migrateSpellBonuses(data)
          elseif fileInfo.file == "inv-config.state" then
            count, migrateErr = migrateConfig(data)
          end

          if not count then
            dbot.warn("Failed to migrate " .. fileInfo.desc .. ": " .. (migrateErr or "unknown"))
            table.insert(results, { desc = fileInfo.desc, status = "FAILED", detail = migrateErr })
            migrationFailed = true
            failureMsg = migrateErr
            return DRL_RET_INTERNAL_ERROR
          else
            table.insert(results, { desc = fileInfo.desc, status = "OK", count = count })
          end
        end -- if data
      end -- if exists
    end -- for

    -- Validate counts
    local validationPassed = true
    for _, r in ipairs(results) do
      if r.status == "OK" and r.count then
        local tableName, expected

        if r.desc == "Items" then
          tableName = "items"
          expected = r.count
        elseif r.desc == "Priorities" then
          tableName = "priorities"
          expected = r.count
        elseif r.desc == "Sets" then
          tableName = "sets"
          expected = r.count
        elseif r.desc == "Snapshots" then
          tableName = "snapshots"
          expected = r.count
        elseif r.desc == "Consumables" then
          tableName = "consumables"
          expected = r.count
        elseif r.desc == "Custom cache" then
          tableName = "cache_custom"
          expected = r.count
        elseif r.desc == "Frequent cache" then
          tableName = "cache_frequent"
          expected = r.count
        elseif r.desc == "Recent cache" then
          tableName = "cache_recent"
          expected = r.count
        elseif r.desc == "Spell bonuses" then
          -- Direct validation against stat_bonuses (equip bonuses no longer migrated
          -- post-v3.0097, so the row count matches the spell migration count directly)
          tableName = "stat_bonuses"
          expected = r.count
        elseif r.desc == "Config" then
          tableName = nil  -- Config uses INSERT OR REPLACE, count may not match exactly
        end

        if tableName then
          local actual, ok = validateCount(tableName, expected)
          if not ok then
            dbot.warn(string.format("Validation failed for %s: expected %d rows, found %d",
                      r.desc, expected, actual))
            r.status = "MISMATCH"
            r.actual = actual
            validationPassed = false
          end
        end
      end
    end

    if not validationPassed then
      migrationFailed = true
      failureMsg = "Count validation failed"
      return DRL_RET_INTERNAL_ERROR
    end

    return DRL_RET_SUCCESS
  end)

  -- Display results
  dbot.print("")
  if retval == DRL_RET_SUCCESS then
    if anyLoadFailed then
      dbot.print("@Y  Migration completed with failures.@w")
      dbot.print("@W  One or more state files could not be read. The categories marked FAILED")
      dbot.print("@W  below were skipped; everything else was imported. Details were written to:")
      dbot.print("@C    " .. getErrorLogPath() .. "@w")
      dbot.print("@W  Restore your previous data with: @Gdinv backup restore pre-migrate@w\n")
    else
      dbot.print("@G  Migration completed successfully!@w\n")
    end
  else
    dbot.print("@R  Migration failed: " .. (failureMsg or "unknown error") .. "@w")
    dbot.print("@R  The migration was rolled back. Your database is now empty.@w")
    dbot.print("@W  Restore your previous data with: @Gdinv backup restore pre-migrate@w\n")
  end

  -- Results table
  dbot.print("@C  Migration Results@w")
  dbot.print("@W  " .. string.rep("-", 45) .. "@w")

  for _, r in ipairs(results) do
    local line
    if r.status == "OK" then
      line = string.format("@W  %-16s @G%5d migrated@w", r.desc, r.count)
    elseif r.status == "MISMATCH" then
      line = string.format("@W  %-16s @R%5d expected, %d found@w", r.desc, r.count, r.actual)
    else
      line = string.format("@W  %-16s @RFAILED@w: %s", r.desc, r.detail or "unknown")
    end
    dbot.print(line)
  end

  dbot.print("")

  if retval == DRL_RET_SUCCESS then
    dbot.info("Reloading plugin to apply migrated data...")
    -- Re-init in-process WITHOUT saving the current state.  The migration wrote
    -- straight to SQLite, so the in-memory tables are still pre-migrate; a full
    -- dbot.reload() would let the unload-time OnPluginSaveState flush that stale
    -- state back over the freshly-migrated database via each module's DELETE-
    -- and-reinsert save(), wiping everything that was just imported.  inv.reload
    -- with drlDoNotSaveState skips the save and reloads from disk instead -- the
    -- same path "dinv backup restore" uses after swapping the database file out-
    -- of-band.
    local reloadRetval = inv.reload(drlDoNotSaveState)
    if (reloadRetval ~= DRL_RET_SUCCESS) then
      dbot.warn("inv.migrate.execute: Failed to reload plugin after migration: " ..
                dbot.retval.getString(reloadRetval))
    end -- if
  end

  return retval
end
